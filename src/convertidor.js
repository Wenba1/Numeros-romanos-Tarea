function convertidor(num) {
    if(num===1){
        return "I";
    }else if(num===2){
        return "II";
    }else if(num===3){
        return "III"
    }else if(num===4){
        return "IV";
    }else if(num===5){
        return "V";
    }else if(num===6){
        return "VI";
    }else if(num===7){
        return "VII";
    }
  }
  
  export default convertidor;
  